class SchemeService:
    async def get_scheme_info(self, query: str, lang: str = "hi") -> str:
        q = query.lower()

        # Check for specific scheme match
        for scheme_key, scheme_data in SCHEMES.items():
            if any(kw in q for kw in scheme_data["keywords"]):
                # Return in user's language, fallback to Hindi, then English
                return (
                    scheme_data.get(lang)
                    or scheme_data.get("hi")
                    or scheme_data.get("en", "")
                )

        # No specific scheme found — return full list in user's language
        return ALL_SCHEMES.get(lang, ALL_SCHEMES["en"])
